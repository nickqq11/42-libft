#include"libft.h"

int ft_atoi(const char *str)
{
int i;
int sum;

i = 0;
sum = 0;
while (str[i] == ' ')
{
    i++;
}
while (str[i] != '\0')
{
    if (str[i] >= 48 && str[i] <= 57)
    {
        sum = sum * 10 + (str[i] - '0');
        i++;
    }
    else
    break;
}
return(sum);
}